// CORE TRANSLATION FUNCTION
// ============================================================

function translateString(enValue, frValue) {
  // If FR is different from EN, keep the existing FR translation
  if (frValue !== enValue) return frValue;

  // FR === EN, so it needs translation
  // 1. Try exact match
  if (EXACT_TRANSLATIONS[enValue]) {
    return EXACT_TRANSLATIONS[enValue];
  }

  // 2. Try case-insensitive exact match
  for (const [key, val] of Object.entries(EXACT_TRANSLATIONS)) {
    if (key.toLowerCase() === enValue.toLowerCase()) {
      // Preserve original casing pattern
      if (enValue === enValue.toUpperCase()) return val.toUpperCase();
      return val;
    }
  }

  // 3. Try phrase patterns
  for (const [pattern, handler] of PHRASE_PATTERNS) {
    const match = enValue.match(pattern);
    if (match) {
      return handler(match);
    }
  }

  // 4. Try word-by-word translation for short strings (< 5 words, no sentences)
  const words = enValue.split(/\s+/);
  if (words.length <= 4 && !/[.!?]/.test(enValue) && !/{{/.test(enValue)) {
    const translated = words.map(w => {
      // Preserve punctuation around words
      const prefix = w.match(/^[^a-zA-Z]*/)?.[0] || '';
      const suffix = w.match(/[^a-zA-Z]*$/)?.[0] || '';
      const core = w.replace(/^[^a-zA-Z]*/, '').replace(/[^a-zA-Z]*$/, '');
      if (!core) return w;
      const tr = translateWord(core);
      // Only return translated if actually different
      if (tr !== core.toLowerCase() && tr !== core) {
        // Preserve capitalization
        if (core[0] === core[0].toUpperCase() && tr[0] === tr[0].toLowerCase()) {
          return prefix + tr.charAt(0).toUpperCase() + tr.slice(1) + suffix;
        }
        return prefix + tr + suffix;
      }
      return w;
    });
    const result = translated.join(' ');
    // Only use if at least one word was actually translated
    if (result !== enValue) return result;
  }

  // 5. No translation found, return EN value (unchanged)
  return enValue;
}

// ============================================================
// RECURSIVE JSON WALKER
// ============================================================

function translateObject(enObj, frObj) {
  const result = {};
  for (const key of Object.keys(enObj)) {
    const enVal = enObj[key];
    const frVal = frObj?.[key];

    if (typeof enVal === 'object' && enVal !== null && !Array.isArray(enVal)) {
      // Recurse into nested objects
      result[key] = translateObject(enVal, typeof frVal === 'object' ? frVal : {});
    } else if (typeof enVal === 'string') {
      result[key] = translateString(enVal, typeof frVal === 'string' ? frVal : enVal);
    } else {
      // Numbers, booleans, arrays, etc. - keep as-is
      result[key] = frVal !== undefined ? frVal : enVal;
    }
  }

  // Also keep any FR-only keys not in EN (in case FR has extra entries)
  if (frObj && typeof frObj === 'object') {
    for (const key of Object.keys(frObj)) {
      if (!(key in result)) {
        result[key] = frObj[key];
      }
    }
  }

  return result;
}

