// Helper: translate a single word using the EXACT map
function translateWord(word) {
  // Try exact match first (case-sensitive)
  if (EXACT_TRANSLATIONS[word]) return EXACT_TRANSLATIONS[word];
  // Try with first letter capitalized
  const capitalized = word.charAt(0).toUpperCase() + word.slice(1);
  if (EXACT_TRANSLATIONS[capitalized]) return EXACT_TRANSLATIONS[capitalized].toLowerCase();
  // Try all lowercase
  const lower = word.toLowerCase();
  for (const [key, val] of Object.entries(EXACT_TRANSLATIONS)) {
    if (key.toLowerCase() === lower) return val.toLowerCase();
  }
  return word;
}

// Helper: "de X" pattern (French genitive)
function translateWithDe(word) {
  const translated = translateWord(word);
  // If it starts with a vowel, use "d'"
  if (/^[aeiouhéèêëàâôîïùû]/i.test(translated)) {
    return `d'${translated}`;
  }
  // Common articles
  if (/^(le |la |l'|les )/i.test(translated)) {
    return translated.replace(/^le /i, 'du ').replace(/^la /i, 'de la ').replace(/^les /i, 'des ').replace(/^l'/i, "de l'");
  }
  return `de ${translated}`;
}

// Helper: "le/la X" pattern (French definite article)
function translateWithLe(word) {
  const translated = translateWord(word);
  if (/^[aeiouhéèêëàâôîïùû]/i.test(translated)) {
    return `l'${translated}`;
  }
  return `le ${translated}`;
}

// Helper: "un/une X" pattern (French indefinite article)
function translateWithUn(word) {
  const translated = translateWord(word);
  return `un ${translated}`;
}

// Helper: "Mon/Ma/Mes X" (my)
function translateMy(word) {
  const translated = translateWord(word);
  // Plural
  if (/s$/i.test(word) && word.length > 2) return `Mes ${translated}`;
  return `Mon ${translated}`;
}

// Helper: "Nouveau/Nouvelle X" (new)
function translateNew(word) {
  const translated = translateWord(word);
  return `Nouveau ${translated}`;
}

