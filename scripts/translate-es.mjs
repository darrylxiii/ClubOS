/**
 * Translate a single English string to Spanish.
 * Preserves {{variables}}, <tags>, brand names.
 */
function translateString(enValue) {
  if (typeof enValue !== 'string') return enValue;

  // Don't translate empty strings
  if (!enValue.trim()) return enValue;

  // Don't translate if it's only variables/HTML/technical tokens
  const stripped = enValue.replace(/\{\{[^}]+\}\}/g, '').replace(/<[^>]+>/g, '').trim();
  if (!stripped) return enValue;

  // Check full phrase map first (exact match)
  if (FULL_PHRASE_MAP.has(enValue)) {
    return FULL_PHRASE_MAP.get(enValue);
  }

  // Check if it's just a single word in the word map
  if (WORD_MAP.has(enValue)) {
    return WORD_MAP.get(enValue);
  }

  // Try case-insensitive match on full phrase map
  for (const [key, val] of FULL_PHRASE_MAP) {
    if (key.toLowerCase() === enValue.toLowerCase()) {
      // Preserve original casing pattern
      return val;
    }
  }

  // --- Intelligent substring/word-based translation ---
  // Extract and preserve {{variables}} and <html> tags
  const preserveTokens = [];
  let working = enValue;

  // Preserve {{variables}}
  working = working.replace(/\{\{[^}]+\}\}/g, (match) => {
    preserveTokens.push(match);
    return `__TOKEN_${preserveTokens.length - 1}__`;
  });

  // Preserve <html> tags
  working = working.replace(/<[^>]+>/g, (match) => {
    preserveTokens.push(match);
    return `__TOKEN_${preserveTokens.length - 1}__`;
  });

  // Try full phrase lookup on the cleaned string
  if (FULL_PHRASE_MAP.has(working)) {
    let result = FULL_PHRASE_MAP.get(working);
    // Restore tokens
    for (let i = 0; i < preserveTokens.length; i++) {
      result = result.replace(`__TOKEN_${i}__`, preserveTokens[i]);
    }
    return result;
  }

  // Word-by-word translation for compound strings
  // Split on word boundaries but preserve punctuation and whitespace
  let translated = working;

  // Sort WORD_MAP keys by length descending so longer matches take priority
  const sortedWords = [...WORD_MAP.entries()].sort((a, b) => b[0].length - a[0].length);

  for (const [eng, esp] of sortedWords) {
    // Use word boundary regex for replacement
    const regex = new RegExp(`\\b${escapeRegex(eng)}\\b`, 'g');
    translated = translated.replace(regex, esp);
  }

  // Restore tokens
  for (let i = 0; i < preserveTokens.length; i++) {
    translated = translated.replace(`__TOKEN_${i}__`, preserveTokens[i]);
  }

  return translated;
}

function escapeRegex(str) {
  return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

