function isSpanglish(esValue, enValue) {
  if (typeof esValue !== 'string' || typeof enValue !== 'string') return false;

  // If it's identical to EN, it's not "Spanglish" - it's untranslated (handled separately)
  if (esValue === enValue) return false;

  // Strip {{variables}}, <tags>, brand names, and technical terms before checking
  let cleaned = esValue
    .replace(/\{\{[^}]+\}\}/g, '')
    .replace(/<[^>]+>/g, '')
    .replace(/\b(?:The Quantum Club|Club OS|Club AI|Club Pilot|Club Radio|QUIN)\b/gi, '');

  // Remove known safe English words (brands, tech terms)
  for (const safe of SAFE_ENGLISH) {
    const safeRegex = new RegExp(`\\b${escapeRegex(safe)}\\b`, 'gi');
    cleaned = cleaned.replace(safeRegex, '');
  }

  const words = cleaned.trim().split(/\s+/).filter(w => w.length > 0);

  // For short strings (1-3 words), check if any word is a known English word
  // that shouldn't be in proper Spanish
  if (words.length <= 3 && words.length > 0) {
    for (const word of words) {
      const lower = word.toLowerCase().replace(/[^a-záéíóúñü]/g, '');
      if (lower && SHORT_ENGLISH_WORDS.has(lower)) {
        return true;
      }
    }
  }

  // For longer strings, use the marker pattern approach
  // Count how many English marker patterns match
  let englishHits = 0;
  for (const pattern of ENGLISH_MARKERS) {
    if (pattern.test(cleaned)) {
      englishHits++;
    }
  }

  // Also check for SHORT_ENGLISH_WORDS in longer strings
  for (const word of words) {
    const lower = word.toLowerCase().replace(/[^a-záéíóúñü]/g, '');
    if (lower && SHORT_ENGLISH_WORDS.has(lower)) {
      englishHits++;
    }
  }

  // For strings with 3+ words, even 1 hit is suspicious if there are other common English words
  if (words.length >= 3) {
    // Check for very common English function words that never appear in Spanish
    const englishFunctionWords = /\b(?:the|an?|is|are|was|were|be|been|being|have|has|had|do|does|did|will|would|could|should|may|might|shall|can|must|need|dare|ought|to|of|in|for|on|at|by|with|from|into|through|during|before|after|above|below|between|under|over|if|or|but|so|yet|nor|not|than|then|when|while|where|how|what|which|who|whom|whose|that|this|these|those|there|here|it|its|he|she|they|them|their|his|her|we|our|us|you|your|my|me|am|isn't|aren't|wasn't|weren't|hasn't|haven't|hadn't|doesn't|don't|didn't|won't|wouldn't|couldn't|shouldn't|can't|mustn't|needn't|no|yes|up|down|out|off|back|away|again|also|too|very|much|more|most|less|least|just|only|still|already|even|never|always|often|sometimes|usually|please|try|check|get|set|let|make|take|give|keep|put|come|go|see|look|find|use|used|using|work|works|working|some|any|all|every|each|both|few|many|other|another|such|next|last|same|different|own|new|old|large|small|big|long|short|high|low|good|bad|right|wrong|first|second|third|sure|able|because|since|until|unless|though|although|however|therefore|otherwise|instead|rather|whether|either|neither|once|twice|enough|quite|rather|available|currently|recently|already|immediately|automatically|successfully|previously|temporarily|permanently|likely|unlikely|possible|impossible|necessary|required|supported|expected|allowed|supposed|unable|below|above)\b/gi;
    const fnMatches = cleaned.match(englishFunctionWords);
    if (fnMatches && fnMatches.length >= 1) {
      englishHits += fnMatches.length;
    }
  }

  // If 2+ English indicators found, it's Spanglish
  return englishHits >= 2;
}

/**
 * Recursively walk EN and ES objects.
 * If ES value === EN value, translate it.
 * If ES value is Spanglish (mixed English/Spanish), re-translate from EN.
 * If ES value is a proper translation, keep it.
 * If key exists in EN but not in ES, translate and add it.
 */
function mergeTranslations(enObj, esObj) {
  const result = {};

  for (const key of Object.keys(enObj)) {
    const enVal = enObj[key];
    const esVal = esObj?.[key];

    if (typeof enVal === 'object' && enVal !== null && !Array.isArray(enVal)) {
      // Recurse into nested objects
      result[key] = mergeTranslations(enVal, typeof esVal === 'object' ? esVal : {});
    } else if (typeof enVal === 'string') {
      if (esVal === undefined || esVal === enVal) {
        // ES is missing or identical to EN — translate it
        result[key] = translateString(enVal);
      } else if (isSpanglish(esVal, enVal)) {
        // ES is contaminated Spanglish — re-translate from EN
        result[key] = translateString(enVal);
      } else {
        // ES already differs and is proper — keep existing translation
        result[key] = esVal;
      }
    } else {
      // Numbers, booleans, arrays, etc. — keep as-is
      result[key] = esVal !== undefined ? esVal : enVal;
    }
  }

  return result;
}

